#include "Button.h"

/***************************************************************************************************
Global Variables:
***************************************************************************************************/
unsigned char BUTTON_FLAG = 0;

/***************************************************************************************************
Function Definitions:
***************************************************************************************************/

void ButtonISR(void)
{
    volatile int * KEY_ptr = (int *) 0xFF200050;  // pushbutton KEY base address
    int press;

    // Global Variable:
    BUTTON_FLAG = 1;

    press = *(KEY_ptr + 3); // read the pushbutton interrupt register
    *(KEY_ptr + 3) = press; // clear the interrupt
}