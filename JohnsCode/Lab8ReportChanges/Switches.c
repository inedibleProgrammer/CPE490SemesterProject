#include "Switches.h"

/***************************************************************************************************
Peripherals:
***************************************************************************************************/
volatile int* SwitchesPtr = (int*)0xFF200040;

/***************************************************************************************************
Function Definitions:
***************************************************************************************************/

double SwitchesPercent()
{
    double switchPercent = ( ( *(SwitchesPtr) & 0x3FF) / 1023.0 );
    return switchPercent;
}